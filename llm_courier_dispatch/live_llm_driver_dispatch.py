ANALYSIS_DIR = ROOT / "analysis" / "courier_dispatch"
CACHE_PATH = ANALYSIS_DIR / "courier_dispatch_llm_driver_cache.json"
VERSION = "courier-llm-driver-v1"


RULE_DESCRIPTIONS = {
    "avoid_long": "You prefer not to take long trips unless compensation or other conditions make it worthwhile.",
    "zone_loyal": "You prefer not to leave your usual zone unless the offer is unusually attractive.",
    "home_pull": "After a deadline, you prefer orders that move you toward home and dislike orders away from home.",
    "surge_only": "You strongly prefer orders with surge pricing and often avoid non-surge orders.",
}


def parse_list(raw: str) -> list[str]:
    return [item.strip() for item in raw.split(",") if item.strip()]


def parse_method_specs(raw: str) -> list[tuple[str, float]]:
    specs: list[tuple[str, float]] = []
    for item in parse_list(raw):
        if ":" in item:
            method, beta = item.split(":", 1)
            specs.append((method.strip(), float(beta)))
        else:
            specs.append((item.strip(), 0.0))
    return specs


def resolved_player_model(model_name: str | None) -> str:
    backend = backend_name("player")
    return model_for("player", backend, model_name)


def cache_key(model: str, payload: dict[str, object]) -> str:
    raw = json.dumps(
        {
            "version": VERSION,
            "backend": os.getenv("LLM_HPGG_BACKEND", "anthropic"),
            "offline": os.getenv("LLM_HPGG_OFFLINE", "0"),
            "model": model,
            "payload": payload,
        },
        sort_keys=True,
    )
    return hashlib.sha256(raw.encode("utf-8")).hexdigest()


def load_cache() -> dict[str, str]:
    if CACHE_PATH.exists():
        return json.loads(CACHE_PATH.read_text(encoding="utf-8"))
    return {}


def save_cache(cache: dict[str, str]) -> None:
    ANALYSIS_DIR.mkdir(parents=True, exist_ok=True)
    CACHE_PATH.write_text(json.dumps(cache, indent=2), encoding="utf-8")


def private_rule_payload(theta: np.ndarray) -> dict[str, object]:
    return {
        name: {
            "active": bool(value),
            "meaning": RULE_DESCRIPTIONS[name],
        }
        for name, value in zip(RULES, theta, strict=True)
    }


def driver_prompt_payload(
    *,
    driver_index: int,
    theta: np.ndarray,
    state: CourierState,
    round_index: int,
    history: list[dict[str, object]],
    max_history: int,
) -> dict[str, object]:
    return {
        "task": "CourierDispatch-Rules private driver decision",
        "driver_index": driver_index,
        "round": round_index,
        "private_operational_rules": private_rule_payload(theta),
        "privacy_instruction": "These private rules are only visible to you as the driver. The platform will observe only your final action code.",
        "public_action_codes": list(ACTIONS),
        "code_warning": "decline-a/b/c/d are neutral platform codes, not literal reasons. Pick the code that best fits your private constraints and the offer.",
        "public_state": compact_state(state),
        "driver_history": history[-max_history:],
        "objective": "Choose the action you would actually take as this driver, balancing pay, route flags, deadline, congestion, menu alternative, and private rules.",
        "response_schema": {"action": "one public_action_code", "reason": "short"},
        "instruction": "Return valid JSON only. The action must exactly match one public_action_code.",
    }


def parse_action(reply: str) -> tuple[int | None, bool]:
    parsed = extract_json(reply)
    raw = parsed.get("action", parsed.get("choice", parsed.get("selected_action")))
    if isinstance(raw, str):
        cleaned = raw.strip().lower()
        for idx, action in enumerate(ACTIONS):
            if cleaned == action:
                return idx, True
        match = re.search(r"(accept|decline-a|decline-b|decline-c|decline-d|reposition|choose-from-menu)", cleaned)
        if match:
            return ACTIONS.index(match.group(1)), False
    if isinstance(raw, int) and 0 <= raw < len(ACTIONS):
        return int(raw), True
    match = re.search(r"\b([0-6])\b", reply)
    if match:
        return int(match.group(1)), False
    return None, False


def call_driver(
    *,
    model: str,
    payload: dict[str, object],
    cache: dict[str, str],
    max_tokens: int,
    temperature: float,
) -> tuple[str, bool]:
    key = cache_key(model, payload)
    if key in cache:
        return cache[key], True
    system = (
        "You are a courier driver in a controlled dispatch benchmark. "
        "Follow your private operational rules and return JSON only."
    )
    reply = call_player(system, json.dumps(payload, indent=2), model=model, max_tokens=max_tokens, temperature=temperature)
    cache[key] = reply
    save_cache(cache)
    return reply, False


def llm_driver_actions(
    *,
    env: CourierDispatchEnv,
    state: CourierState,
    seed: int,
    round_index: int,
    candidate_index: int,
    model: str,
    cache: dict[str, str],
    histories: list[list[dict[str, object]]],
    max_history: int,
    max_tokens: int,
    temperature: float,
) -> tuple[np.ndarray, list[dict[str, object]]]:
    actions = np.zeros(env.n_agents, dtype=int)
    traces: list[dict[str, object]] = []
    for driver in range(env.n_agents):
        payload = driver_prompt_payload(
            driver_index=driver,
            theta=env.true_types[driver],
            state=state,
            round_index=round_index,
            history=histories[driver],
            max_history=max_history,
        )
        reply, cache_hit = call_driver(model=model, payload=payload, cache=cache, max_tokens=max_tokens, temperature=temperature)
        parsed_action, parsed_ok = parse_action(reply)
        if parsed_action is None:
            fallback_rng = deterministic_rng(seed, round_index, candidate_index, driver, f"llm-driver-fallback-{model}")
            parsed_action = env.simulate_action(state, env.true_types[driver], fallback_rng, agent=driver, all_types=env.true_types)
        actions[driver] = int(parsed_action)
        traces.append(
            {
                "driver": driver,
                "action": ACTIONS[int(parsed_action)],
                "parsed_ok": parsed_ok,
                "cache_hit": cache_hit,
                "reply": reply,
            }
        )
    return actions, traces


def run_llm_driver_episode(
    *,
    method: str,
    beta: float,
    driver_model: str,
    seed: int,
    n_agents: int,
    rule_count: int,
    horizon: int,
    tau: float,
    penalty_scale: float,
    home_scale: float,
    menu_friction: float,
    candidate_count: int,
    cache: dict[str, str],
    max_history: int,
    max_tokens: int,
    temperature: float,
) -> list[dict[str, object]]:
    rng = np.random.default_rng(seed + 500_000)
    env = CourierDispatchEnv(
        n_agents=n_agents,
        rule_count=rule_count,
        horizon=horizon,
        tau=tau,
        penalty_scale=penalty_scale,
        home_scale=home_scale,
        menu_friction=menu_friction,
        seed=seed,
    )
    env.reset(seed)
    posteriors = [RulePosterior(env.type_space) for _ in range(env.n_agents)]
    joint_posterior = JointRulePosterior(env.type_space, env.n_agents) if method == "joint_psrl" else None
    verbal_belief: list[np.ndarray] | None = None
    driver_histories: list[list[dict[str, object]]] = [[] for _ in range(env.n_agents)]
    rows: list[dict[str, object]] = []
    cumulative_reward = 0.0
    cumulative_probe_cost = 0.0
    for round_index in range(horizon):
        candidates = env.sample_candidate_states(candidate_count)
        if joint_posterior is not None:
            posteriors = joint_posterior.marginal_posteriors()
        if method == "oracle":
            candidate_actions = []
            candidate_traces = []
            candidate_rewards = []
            for candidate_index, candidate in enumerate(candidates):
                actions, traces = llm_driver_actions(
                    env=env,
                    state=candidate,
                    seed=seed,
                    round_index=round_index,
                    candidate_index=candidate_index,
                    model=driver_model,
                    cache=cache,
                    histories=driver_histories,
                    max_history=max_history,
                    max_tokens=max_tokens,
                    temperature=temperature,
                )
                candidate_actions.append(actions)
                candidate_traces.append(traces)
                candidate_rewards.append(env.reward_fn(candidate, actions, env.true_types))
            candidate_mean_rewards = [float(np.mean(rewards)) for rewards in candidate_rewards]
            choice_idx = int(np.argmax(candidate_mean_rewards))
            choice = PlanChoice(candidates[choice_idx], candidate_mean_rewards[choice_idx], 0.0, candidate_mean_rewards[choice_idx], candidate_mean_rewards[choice_idx], 0.0, choice_idx)
            observed_actions = candidate_actions[choice_idx]
            driver_traces = candidate_traces[choice_idx]
        else:
            sampled_override = joint_posterior.sample(rng) if joint_posterior is not None else None
            choice = choose_state(env, candidates, posteriors, rng, method, beta, verbal_belief, sampled_types_override=sampled_override)
            observed_actions, driver_traces = llm_driver_actions(
                env=env,
                state=choice.state,
                seed=seed,
                round_index=round_index,
                candidate_index=choice.candidate_index,
                model=driver_model,
                cache=cache,
                histories=driver_histories,
                max_history=max_history,
                max_tokens=max_tokens,
                temperature=temperature,
            )
        rewards = env.reward_fn(choice.state, observed_actions, env.true_types)
        if method in {"pact", "pact_plus", "map_greedy"}:
            for driver in range(env.n_agents):
                posteriors[driver].update(env, choice.state, int(observed_actions[driver]))
        elif joint_posterior is not None:
            joint_posterior.update(env, choice.state, observed_actions)
            posteriors = joint_posterior.marginal_posteriors()
        elif method == "atom_tom1":
            verbal_belief = update_verbal_belief(env, choice.state, observed_actions, rng)
        cumulative_reward += float(np.mean(rewards))
        cumulative_probe_cost += choice.exploration_cost
        for driver in range(env.n_agents):
            driver_histories[driver].append(
                {
                    "round": round_index,
                    "public_state": compact_state(choice.state),
                    "action": ACTIONS[int(observed_actions[driver])],
                    "reward": float(rewards[driver]),
                }
            )
        if method == "oracle":
            mean_true_prob = 1.0
            mean_rule_acc = 1.0
        elif joint_posterior is not None:
            mean_true_prob = joint_posterior.prob_true(env.true_types)
            mean_rule_acc = joint_posterior.rule_marginal_accuracy(env.true_types)
        else:
            true_probs = [posterior.prob_true(env.true_types[i]) for i, posterior in enumerate(posteriors)]
            rule_acc = [posterior.rule_marginal_accuracy(env.true_types[i]) for i, posterior in enumerate(posteriors)]
            mean_true_prob = float(np.mean(true_probs))
            mean_rule_acc = float(np.mean(rule_acc))
        rows.append(
            {
                "driver_model": driver_model,
                "method": method,
                "beta": beta,
                "seed": seed,
                "round": round_index,
                "mean_reward": float(np.mean(rewards)),
                "cumulative_reward": cumulative_reward,
                "exploration_cost": choice.exploration_cost,
                "cumulative_exploration_cost": cumulative_probe_cost,
                "mean_true_tuple_posterior": mean_true_prob,
                "mean_rule_marginal_accuracy": mean_rule_acc,
                "chosen_candidate": choice.candidate_index,
                "observed_actions": [ACTIONS[int(action)] for action in observed_actions],
                "driver_parse_ok_rate": float(np.mean([trace["parsed_ok"] for trace in driver_traces])),
                "driver_cache_hit_rate": float(np.mean([trace["cache_hit"] for trace in driver_traces])),
            }
        )
    return rows


def summarize(rows: list[dict[str, object]]) -> list[dict[str, object]]:
    grouped: dict[tuple[str, str, float], list[dict[str, object]]] = {}
    for row in rows:
        grouped.setdefault((str(row["driver_model"]), str(row["method"]), float(row["beta"])), []).append(row)
    summary: list[dict[str, object]] = []
    for (driver_model, method, beta), group in sorted(grouped.items()):
        final_round = max(int(row["round"]) for row in group)
        final = [row for row in group if int(row["round"]) == final_round]
        summary.append(
            {
                "driver_model": driver_model,
                "method": method,
                "beta": beta,
                "episodes": len({int(row["seed"]) for row in group}),
                "final_mean_cumulative_reward": float(np.mean([float(row["cumulative_reward"]) for row in final])),
                "final_mean_true_tuple_posterior": float(np.mean([float(row["mean_true_tuple_posterior"]) for row in final])),
                "final_mean_rule_marginal_accuracy": float(np.mean([float(row["mean_rule_marginal_accuracy"]) for row in final])),
                "final_mean_cumulative_exploration_cost": float(np.mean([float(row["cumulative_exploration_cost"]) for row in final])),
                "driver_parse_ok_rate": float(np.mean([float(row["driver_parse_ok_rate"]) for row in group])),
                "driver_cache_hit_rate": float(np.mean([float(row["driver_cache_hit_rate"]) for row in group])),
            }
        )
    return summary


def write_rows_csv(path: Path, rows: list[dict[str, object]]) -> None:
    fields = [
        "driver_model",
        "method",
        "beta",
        "seed",
        "round",
        "mean_reward",
        "cumulative_reward",
        "exploration_cost",
        "cumulative_exploration_cost",
        "mean_true_tuple_posterior",
        "mean_rule_marginal_accuracy",
        "chosen_candidate",
        "driver_parse_ok_rate",
        "driver_cache_hit_rate",
    ]
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fields)
        writer.writeheader()
        for row in rows:
            writer.writerow({field: row.get(field, "") for field in fields})


def write_summary_csv(path: Path, summary: list[dict[str, object]]) -> None:
    fields = list(summary[0].keys()) if summary else []
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fields)
        writer.writeheader()
        writer.writerows(summary)


def run(args: argparse.Namespace) -> dict[str, object]:
    if args.backend:
        os.environ["LLM_HPGG_BACKEND"] = args.backend
    if args.offline:
        os.environ["LLM_HPGG_OFFLINE"] = "1"
    cache = load_cache()
    methods = parse_method_specs(args.methods)
    driver_models = [resolved_player_model(model) for model in args.driver_models]
    rows: list[dict[str, object]] = []
    for driver_model in driver_models:
        for seed in range(args.seeds):
            for method, beta in methods:
                rows.extend(
                    run_llm_driver_episode(
                        method=method,
                        beta=beta,
                        driver_model=driver_model,
                        seed=args.seed_offset + seed,
                        n_agents=args.n_agents,
                        rule_count=args.rule_count,
                        horizon=args.horizon,
                        tau=args.tau,
                        penalty_scale=args.penalty_scale,
                        home_scale=args.home_scale,
                        menu_friction=args.menu_friction,
                        candidate_count=args.candidates,
                        cache=cache,
                        max_history=args.max_history,
                        max_tokens=args.max_tokens,
                        temperature=args.temperature,
                    )
                )
    save_cache(cache)
    summary = summarize(rows)
    payload = {
        "setting": {
            "backend": os.getenv("LLM_HPGG_BACKEND", "anthropic"),
            "driver_models": driver_models,
            "methods": [[method, beta] for method, beta in methods],
            "horizon": args.horizon,
            "seeds": args.seeds,
            "candidate_states_per_round": args.candidates,
            "n_agents": args.n_agents,
            "rule_count": args.rule_count,
            "actions": list(ACTIONS),
            "tau": args.tau,
            "version": VERSION,
            "cache_path": str(CACHE_PATH.relative_to(ROOT)),
        },
        "summary": summary,
        "rows": rows,
    }
    ANALYSIS_DIR.mkdir(parents=True, exist_ok=True)
    summary_path = ANALYSIS_DIR / f"{args.out_prefix}_summary.json"
    rows_csv = ANALYSIS_DIR / f"{args.out_prefix}_rows.csv"
    summary_csv = ANALYSIS_DIR / f"{args.out_prefix}_summary.csv"
    summary_path.write_text(json.dumps(payload, indent=2), encoding="utf-8")
    write_rows_csv(rows_csv, rows)
    write_summary_csv(summary_csv, summary)
    return {"summary_path": str(summary_path.relative_to(ROOT)), "rows_csv": str(rows_csv.relative_to(ROOT)), "summary_csv": str(summary_csv.relative_to(ROOT)), "payload": payload}


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--backend", default="cloudgpt")
    parser.add_argument("--driver-models", type=parse_list, default=parse_list("gpt-5.4-mini-20260317"))
    parser.add_argument("--methods", default="pact:0.0,pact_plus:0.1,map_greedy:0.0,joint_psrl:0.0,atom_tom1:0.0,random:0.0,oracle:0.0")
    parser.add_argument("--n-agents", type=int, default=3)
    parser.add_argument("--rule-count", type=int, default=4)
    parser.add_argument("--horizon", type=int, default=4)
    parser.add_argument("--seeds", type=int, default=1)
    parser.add_argument("--seed-offset", type=int, default=0)
    parser.add_argument("--tau", type=float, default=0.5)
    parser.add_argument("--penalty-scale", type=float, default=2.0)
    parser.add_argument("--home-scale", type=float, default=1.2)
    parser.add_argument("--menu-friction", type=float, default=0.2)
    parser.add_argument("--candidates", type=int, default=3)
    parser.add_argument("--max-history", type=int, default=4)
    parser.add_argument("--max-tokens", type=int, default=180)
    parser.add_argument("--temperature", type=float, default=0.2)
    parser.add_argument("--out-prefix", default="courier_dispatch_llm_driver")
    parser.add_argument("--offline", action="store_true")
    args = parser.parse_args()
    result = run(args)
    printable = {key: value for key, value in result.items() if key != "payload"}
    print(json.dumps(printable, indent=2))
    for row in result["payload"]["summary"]:
        print(
            f"{row['driver_model']} | {row['method']} beta={row['beta']}: "
            f"reward={row['final_mean_cumulative_reward']:.3f} "
            f"Ptrue={row['final_mean_true_tuple_posterior']:.3f} "
            f"rule_acc={row['final_mean_rule_marginal_accuracy']:.3f} "
            f"parse={row['driver_parse_ok_rate']:.3f}"
        )


if __name__ == "__main__":
    main()