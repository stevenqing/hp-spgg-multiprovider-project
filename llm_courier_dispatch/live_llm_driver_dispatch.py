"""CourierDispatch variant with live LLMs as private-rule drivers.

The platform still chooses public candidate states with PACT/MAP/PSRL/A-ToM
style policies. The difference from the default benchmark is that observed
driver actions are generated by an LLM that receives its private rule tuple.
The platform never sees those private rules; it only sees neutral action codes.
"""

